// --- Language Switcher ---
const langBtn = document.getElementById('lang-toggle');

function updateContent() {
  const dict = i18n[currentLang];
  
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key]) {
      el.placeholder = dict[key];
    }
  });

  // Re-trigger visual updates if values exist
  if (document.getElementById('password').value) {
    document.getElementById('password').dispatchEvent(new Event('input'));
  }
  if (document.getElementById('motivation').value) {
    document.getElementById('motivation').dispatchEvent(new Event('input'));
  }

  // Clear messages as they might be in wrong language
  const inputs = ['fname', 'lname', 'email', 'age', 'track', 'github', 'motivation', 'pw', 'confirm'];
  inputs.forEach(id => clearMsg(id));
}

langBtn.addEventListener('click', () => {
  if (currentLang === 'ar') {
    currentLang = 'en';
    document.documentElement.setAttribute('lang', 'en');
    document.documentElement.setAttribute('dir', 'ltr');
    langBtn.textContent = 'AR';
  } else {
    currentLang = 'ar';
    document.documentElement.setAttribute('lang', 'ar');
    document.documentElement.setAttribute('dir', 'rtl');
    langBtn.textContent = 'EN';
  }
  updateContent();
});

// --- Validation and Logic ---
const liveRegion = document.getElementById('live-region');

function getDict() {
  return i18n[currentLang];
}

function announce(msg) {
  liveRegion.textContent = '';
  setTimeout(() => { liveRegion.textContent = msg; }, 50);
}

function setError(id, msg) {
  const el = document.getElementById(id + '-msg');
  const input = document.getElementById(id) || document.querySelector('[name="' + id + '"]');
  if (el) { el.textContent = msg; el.className = 'field-msg error'; }
  if (input) { input.setAttribute('aria-invalid', 'true'); }
}

function setSuccess(id, msg) {
  const el = document.getElementById(id + '-msg');
  const input = document.getElementById(id) || document.querySelector('[name="' + id + '"]');
  if (el) { el.textContent = msg; el.className = 'field-msg success'; }
  if (input) { input.setAttribute('aria-invalid', 'false'); }
}

function clearMsg(id) {
  const el = document.getElementById(id + '-msg');
  const input = document.getElementById(id);
  if (el) { el.textContent = ''; el.className = 'field-msg'; }
  if (input) { input.removeAttribute('aria-invalid'); }
}

// === SECTION 1 VALIDATORS ===
function validateFname() {
  const v = document.getElementById('fname').value.trim();
  const d = getDict();
  if (!v) { setError('fname', d.err_fname_req); return false; }
  if (v.length < 2) { setError('fname', d.err_fname_len); return false; }
  if (/\d/.test(v)) { setError('fname', d.err_fname_num); return false; }
  setSuccess('fname', d.succ_fname); return true;
}

function validateLname() {
  const v = document.getElementById('lname').value.trim();
  const d = getDict();
  if (!v) { setError('lname', d.err_lname_req); return false; }
  if (v.length < 2) { setError('lname', d.err_lname_len); return false; }
  if (/\d/.test(v)) { setError('lname', d.err_lname_num); return false; }
  setSuccess('lname', d.succ_lname); return true;
}

function validateEmail() {
  const v = document.getElementById('email').value.trim();
  const regex = /^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/;
  const d = getDict();
  if (!v) { setError('email', d.err_email_req); return false; }
  if (!regex.test(v)) { setError('email', d.err_email_fmt); return false; }
  setSuccess('email', d.succ_email); return true;
}

function validateAge() {
  const v = parseInt(document.getElementById('age').value);
  const d = getDict();
  if (!v) { setError('age', d.err_age_req); return false; }
  if (v < 16) { setError('age', d.err_age_min); return false; }
  if (v > 50) { setError('age', d.err_age_max); return false; }
  setSuccess('age', d.succ_age); return true;
}

function validateTrack() {
  const v = document.getElementById('track').value;
  const d = getDict();
  if (!v) { setError('track', d.err_track); return false; }
  setSuccess('track', d.succ_track); return true;
}

// === SECTION 2 VALIDATORS ===
function validateLevel() {
  const checked = document.querySelector('input[name="level"]:checked');
  const msg = document.getElementById('level-msg');
  const d = getDict();
  if (!checked) {
    if (msg) { msg.textContent = d.err_level; msg.className = 'field-msg error'; }
    return false;
  }
  if (msg) { msg.textContent = ''; msg.className = 'field-msg'; }
  return true;
}

function validateGithub() {
  const v = document.getElementById('github').value.trim();
  const regex = /^https:\/\/github\.com\/[a-zA-Z0-9\-_]+\/?$/;
  const d = getDict();
  if (!v) { setError('github', d.err_git_req); return false; }
  if (!regex.test(v)) { setError('github', d.err_git_fmt); return false; }
  setSuccess('github', d.succ_git); return true;
}

function validateMotivation() {
  const v = document.getElementById('motivation').value.trim();
  const d = getDict();
  if (!v) { setError('motivation', d.err_motv_req); return false; }
  if (v.length < 80) { setError('motivation', d.err_motv_len + v.length + '/80'); return false; }
  setSuccess('motivation', d.succ_motv); return true;
}

// === SECTION 3 VALIDATORS ===
const PW_REGEX = /^(?=.*[A-Z])(?=.*[a-z])(?=.*[0-9])(?=.*[#?!@$%^&*\-]).{8,}$/;

function checkPwRules(pw) {
  const rules = {
    len: pw.length >= 8,
    upper: /[A-Z]/.test(pw),
    lower: /[a-z]/.test(pw),
    num: /[0-9]/.test(pw),
    special: /[#?!@$%^&*\-]/.test(pw)
  };
  Object.keys(rules).forEach(k => {
    const el = document.getElementById('rule-' + k);
    if (el) el.classList.toggle('met', rules[k]);
  });
  return rules;
}

function getStrength(pw) {
  const r = checkPwRules(pw);
  const score = Object.values(r).filter(Boolean).length;
  const d = getDict();
  if (pw.length === 0) return { score: 0, label: '', color: '' };
  if (score <= 2) return { score: 20, label: d.pw_weak, color: '#ff5c5c' };
  if (score === 3) return { score: 50, label: d.pw_med, color: '#f5a623' };
  if (score === 4) return { score: 75, label: d.pw_good, color: '#4a90e2' };
  return { score: 100, label: d.pw_excel, color: '#00c896' };
}

function validatePassword() {
  const v = document.getElementById('password').value;
  const d = getDict();
  if (!v) { setError('pw', d.err_pw_req); return false; }
  if (!PW_REGEX.test(v)) { setError('pw', d.err_pw_fmt); return false; }
  setSuccess('pw', d.succ_pw); return true;
}

function validateConfirm() {
  const pw = document.getElementById('password').value;
  const cf = document.getElementById('confirm').value;
  const d = getDict();
  if (!cf) { setError('confirm', d.err_conf_req); return false; }
  if (pw !== cf) { setError('confirm', d.err_conf_match); return false; }
  setSuccess('confirm', d.succ_conf); return true;
}

