document.getElementById('password').addEventListener('input', function() {
  const pw = this.value;
  const s = getStrength(pw);
  document.getElementById('strength-fill').style.width = s.score + '%';
  document.getElementById('strength-fill').style.background = s.color;
  document.getElementById('strength-text').textContent = s.label;
  document.getElementById('strength-text').style.color = s.color;
  if (document.getElementById('confirm').value) validateConfirm();
  clearMsg('pw');
});

document.getElementById('motivation').addEventListener('input', function() {
  const len = this.value.length;
  document.getElementById('char-val').textContent = len;
  const counter = document.getElementById('motivation-counter');
  counter.className = 'char-counter' + (len > 480 ? ' warn' : '') + (len >= 500 ? ' over' : '');
  if (len >= 80) clearMsg('motivation');
});

// === NAVIGATION ===
function goToSection(num) {
  const currentSection = document.querySelector('.form-section.active');
  const currentNum = parseInt(currentSection.id.split('-')[1]);
  const d = getDict();

  if (num > currentNum) {
    let valid = true;
    if (currentNum === 1) {
      valid = validateFname() & validateLname() & validateEmail() & validateAge() & validateTrack();
    } else if (currentNum === 2) {
      valid = validateLevel() & validateGithub() & validateMotivation();
    }
    if (!valid) {
      announce(currentLang === 'ar' ? 'يوجد أخطاء في النموذج. يرجى تصحيحها قبل المتابعة.' : 'There are errors in the form. Please correct them to continue.');
      return;
    }
  }

  currentSection.classList.remove('active');
  document.getElementById('section-' + num).classList.add('active');

  // Update progress
  for (let i = 1; i <= 3; i++) {
    const circle = document.getElementById('step-circle-' + i);
    const label = document.getElementById('step-label-' + i);
    const line = document.getElementById('step-line-' + i);
    if (i < num) {
      circle.classList.add('done'); circle.classList.remove('active');
      circle.textContent = '✓';
      if (label) { label.classList.add('done'); label.classList.remove('active'); }
      if (line) line.classList.add('done');
    } else if (i === num) {
      circle.classList.add('active'); circle.classList.remove('done');
      circle.textContent = ['١', '٢', '٣'][i - 1]; // English numbers? Let's keep it AR logic or switch based on lang
      if (currentLang === 'en') {
        circle.textContent = i;
      }
      if (label) { label.classList.add('active'); label.classList.remove('done'); }
      circle.setAttribute('aria-current', 'step');
    } else {
      circle.classList.remove('active', 'done');
      circle.textContent = currentLang === 'ar' ? ['١', '٢', '٣'][i - 1] : i;
      circle.removeAttribute('aria-current');
      if (label) { label.classList.remove('active', 'done'); }
      if (line) line.classList.remove('done');
    }
  }

  window.scrollTo({ top: 0, behavior: 'smooth' });
  announce(currentLang === 'ar' ? 'الانتقال إلى الخطوة ' + num : 'Moving to step ' + num);
}

// === SUBMIT ===
function submitForm() {
  const valid = validatePassword() & validateConfirm();
  const d = getDict();
  if (!valid) {
    announce(currentLang === 'ar' ? 'يوجد أخطاء في كلمة المرور. يرجى تصحيحها.' : 'There are errors in the password. Please correct them.');
    return;
  }

  const btn = document.getElementById('submit-btn');
  btn.disabled = true;
  btn.innerHTML = d.loading;

  setTimeout(() => {
    document.getElementById('section-3').classList.remove('active');

    const appId = 'DL-2026-' + Math.random().toString(36).substr(2, 6).toUpperCase();
    const fname = document.getElementById('fname').value.trim();
    const lname = document.getElementById('lname').value.trim();
    const email = document.getElementById('email').value.trim();
    const track = document.getElementById('track');
    const trackText = track.options[track.selectedIndex].text;
    const level = document.querySelector('input[name="level"]:checked');
    const levelText = level ? document.querySelector('label[for="' + level.id + '"]').textContent : '—';

    document.getElementById('app-id').textContent = d.app_id_prefix + appId;

    document.getElementById('summary-card').innerHTML = `
      <div class="summary-row"><span class="summary-key" data-i18n="summary_name">${d.summary_name}</span><span class="summary-val">${fname} ${lname}</span></div>
      <div class="summary-row"><span class="summary-key" data-i18n="summary_email">${d.summary_email}</span><span class="summary-val">${email}</span></div>
      <div class="summary-row"><span class="summary-key" data-i18n="summary_track">${d.summary_track}</span><span class="summary-val">${trackText}</span></div>
      <div class="summary-row"><span class="summary-key" data-i18n="summary_level">${d.summary_level}</span><span class="summary-val">${levelText}</span></div>
    `;

    document.getElementById('success-page').classList.add('active');
    announce((currentLang === 'ar' ? 'تم إرسال طلبك بنجاح. رقم الطلب: ' : 'Application submitted successfully. ID: ') + appId);
    window.scrollTo({ top: 0, behavior: 'smooth' });
  }, 1200);
}
